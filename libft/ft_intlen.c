#include "libft.h"

int		ft_intlen(long long n)
{
	int	res;

	res = 0;
	if (n == (long long)-9223372036854775807 - 1)
	{
		res = 20;
		return (res);
	}
	if (n < 0)
	{
		n = -n;
		res++;
	}
	while (n >= 10)
	{
		n = n / 10;
		res++;
	}
	res++;
	return (res);
}
